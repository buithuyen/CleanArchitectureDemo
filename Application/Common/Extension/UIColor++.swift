import UIKit

extension UIColor {
    class func main() -> UIColor {
        return UIColor.purple
    }

    class func background() -> UIColor {
        return UIColor.darkGray
    }

    class func titleColor() -> UIColor {
        return UIColor.white
    }

    class func descriptionColor() -> UIColor {
        return UIColor.gray
    }
}
